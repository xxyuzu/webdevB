<?php


// 次の条件に従って、PHPでスコア判定プログラムを作成しなさい。
// 0から100までのランダムな整数を変数$scoreに代入する。
// $score = rand(0, 100);
// 「スコア: ○○」の形式でスコアを表示する。
// スコアが80以上なら「優」、60以上80未満なら「良」、それ以外は「可」と表示する。

$score = rand(0, 100); // 0から100までのランダムな整数を生成
echo "スコア: $score\n"; // スコアを表示
if ($score >= 80) {
    echo "優"; // スコアが80以上なら「優」
} elseif ($score >= 60) {
    echo "良"; // スコアが60以上80未満なら「良」
} else {
    echo "可"; // それ以外は「可」
}
